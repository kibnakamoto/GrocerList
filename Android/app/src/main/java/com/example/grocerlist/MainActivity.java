package com.example.grocerlist;

import android.graphics.Paint;
import android.os.Bundle;
import android.view.View;
import android.widget.CheckBox;
import android.widget.EditText;

import androidx.appcompat.app.AppCompatActivity;

public class MainActivity extends AppCompatActivity {
    @Override
    protected void onCreate(Bundle savedInstanceState) {
        super.onCreate(savedInstanceState);
        setContentView(R.layout.activity_main);

        // Find views by their respective IDs
        EditText searchBar = findViewById(R.id.searchBar);
        CheckBox strikeCheckBox = findViewById(R.id.strikeCheckBox);

        // Set click listener for the checkbox
        strikeCheckBox.setOnClickListener(new View.OnClickListener() {
            @Override
            public void onClick(View view) {
                // Add your checkbox click logic here
                if (strikeCheckBox.isChecked()) {
                    // If checkbox is checked, apply strike-through to the checkbox's text
                    strikeCheckBox.setPaintFlags(strikeCheckBox.getPaintFlags() | Paint.STRIKE_THRU_TEXT_FLAG);
                } else {
                    // If checkbox is unchecked, remove strike-through from the checkbox's text
                    strikeCheckBox.setPaintFlags(strikeCheckBox.getPaintFlags() & ~Paint.STRIKE_THRU_TEXT_FLAG);
                }
            }
        });
    }
}

